package ru.itpark.component;

public class MessageSource {

    private String basename;

    private String defaultDecoding;

    public String getBasename() {
        return basename;
    }

    public void setBasename(String basename) {
        this.basename = basename;
    }

    public String getDefaultDecoding() {
        return defaultDecoding;
    }

    public void setDefaultDecoding(String defaultDecoding) {
        this.defaultDecoding = defaultDecoding;
    }
}
